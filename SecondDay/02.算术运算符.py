"""
+ -
*: 乘法
/: 除法
%: 取余(取模)
//:取整
**: 取幂
"""

print(10 * 20) # 200
a = 10
b = 5
print(a * b) # 50

# 两个整数相除得到的小数
print(20 / 10) # 2.0
print(3.14 / 2.0) # 1.57

print(10 % 3) # 1
print(5 % 2) #1
print(3 % 3) # 0

# 一个数字余上2结果为0，这个数字一定是2的倍数（一定一个偶数）
print(0 % 2)
print(2 % 2)
print(10 % 2)

print(10 // 3) # 3

print(2 ** 6) # 64