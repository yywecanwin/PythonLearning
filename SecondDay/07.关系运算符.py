"""
关系(比较,条件,判断)运算符
    > < >= <= == !=
    关系运算符 运算的结果 是一个 布尔 类型的值,要么是True,要么是False.
"""
a = 10
b = 20
c = 30

print(a > b) # False
print(b > c) # False
print(a < c) # True
print(a == c) # False
print(a == 10) # True

print(a <= c) # True
print(a <= 5) # False