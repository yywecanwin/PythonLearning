# 字符串与整数不能相加
# "hello" + 10

# 把字符串赋值n次，就是直接拼接n次
print("hello" * 2)

# 两个字符串可以相加，这里的 + 字符串连接的意思，用来连接连个字符串的
print("hello" + "xuzhou") #helloxuzhou
print("hello" + "python" +"java" ) # hellopythonjava

# 布尔类型的值可以与 数字直接运算, True作为1,False作为0
print(True + 1) # 2
print(False + 1) # 1
