"""
列表：
    也是一个容器，可以存储多个元素，元素可以是任何类型的
定义格式：
    #非空
    列表名 = [元素1，元素2，。。。。]
    空列表
    列表名 = []
"""
name_list = ["yao",'is','a','good','man']
print(name_list) # ['yao', 'is', 'a', 'good', 'man']
print(type(name_list)) # <class 'list'>


list1 = [10, 3.14, "hello", True]
print(list1) # [10, 3.14, 'hello', True]

#定义空列表
list2 = []
print(list2) # []

