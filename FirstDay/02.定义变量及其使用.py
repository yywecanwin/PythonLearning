"""
定义变量及其使用
    定义格式：
        变量名 = 数据值

"""

# 第一次给变量赋值：定义变量，并赋值
# 这里的 = 是赋值的意思，是把 = 右侧的数据赋值给左侧的变量
a = 10

#变量：打印的那一块区域中的数据
print(a) # 10

# 再次给变量赋值：不是在定义变量，而是在修改变量的值
a = 20
print(a) # 20

# 使用变量与其他数据进行运算
# 先计算a + 30,得到一个结果50，然后调用python解释器自带的打印功能，打印50
print(a + 30) #50

# 定义变量b，把a变量的值20赋值给变量b
b = a
print(b)









